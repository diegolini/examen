    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         String[] arr1 = conj1.getText().split(",");
    Set<String> A = new HashSet<>(Arrays.asList(arr1));
    A.remove(""); 
    
    List<String> lista = new ArrayList<>(A);
    List<String> resultado = new ArrayList<>();
    int n = lista.size();
    int total = 1 << n; // 2^n
    
    for (int i = 0; i < total; i++) {
        List<String> subconjunto = new ArrayList<>();
        for (int j = 0; j < n; j++) {
            if ((i & (1 << j)) != 0) {
                subconjunto.add(lista.get(j));
            }
        }
        resultado.add(subconjunto.toString());
    }
    
    jTextPane2.setText("Conjunto Potencia: " + resultado.toString());
    }//GEN-LAST:event_jButton4ActionPerformed

